def report(name, items, limit):
    print(f"\n=== {name} (limit {limit}) ===")
    over = 0
    for i, s in enumerate(items, 1):
        n = len(s)
        flag = "OK " if n <= limit else f"OVER+{n-limit}"
        print(f"{i:02d}. [{n:>3}] {flag}  {s}")
        if n > limit:
            over += 1
    print(f"-- {over} over-limit --")


